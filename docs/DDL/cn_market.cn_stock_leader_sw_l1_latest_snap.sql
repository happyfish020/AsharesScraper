    PRIMARY KEY (`trade_date`, `symbol`),
    KEY `idx_cn_stock_leader_sw_l1_latest_snap_symbol` (`symbol`),
    KEY `idx_cn_stock_leader_sw_l1_latest_snap_score` (`trade_date`, `leader_score`),
    KEY `idx_cn_stock_leader_sw_l1_latest_snap_sw` (`trade_date`, `sw_l1_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;
